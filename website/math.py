"""
math.py: dedicated to performing basic arithmetic operations
"""


def add(x: int, y: int) -> int:
    """
    Adds two numbers
    params:
        x: int
        y: int
    returns:
        sum: int
    """
    return x + y


def subtract(x: int, y: int) -> int:
    """
    Subtracts two numbers
    params:
        x: int
        y: int
    returns:
        difference: int
    """
    return x + y
