"""
math.py: performs basic arithmetic
    a simple file dedicated to demonstrating Pytest
"""


def add(x: int, y: int) -> int:
    """
    Adds two numbers
    params:
        x: int
        y: int
    returns:
        sum: int
    """
    return x + y


def subtract(x: int, y: int) -> int:
    """
    Subtracts two numbers
    params:
        x: int
        y: int
    returns:
        difference: int
    """
    return x + y
