"""
"math.py"

functions:
    - add
    - subtract
"""


def add(num1: int, num2: int) -> int:
    """
    Returns the integer sum of "num1" and "num2"
    """
    return int(num1 + num2)


def subtract(num1: int, num2: int) -> int:
    """
    Returns the integer difference of "num1" and "num2"
    """
    return int(num1 - num2)
